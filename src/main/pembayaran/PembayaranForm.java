    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        String id = JOptionPane.showInputDialog(null, "Cari Berdasarkan Kode Pembayaran!!!");
        try{
            String sql = "SELECT * FROM pembayaran "
                    + "INNER JOIN pemeriksaan ON pemeriksaan.kode_periksa=pembayaran.kode_periksa "
                    + "INNER JOIN pasien ON pasien.id_pasien=pemeriksaan.id_pasien "
                    + "INNER JOIN dokter ON dokter.id_dokter=pemeriksaan.id_dokter "
                    + "WHERE id='"+id+"'";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            if(res.next()){
                JOptionPane.showMessageDialog(null, "Data ditemukan");
                txtkodepembayaran.setText(res.getString(1));
                txtkodeperiksa.setText(res.getString(2));
                txtnamapasien.setText(res.getString(13));
                txtnamadokter.setText(res.getString(19));
                txttindakan.setText(res.getString(11));
                txttotal.setText(res.getString(3));
                txtbayar.setText(res.getString(4));
                txtkembalian.setText(res.getString(5));
                
                
                
                btnsimpan.setEnabled(false);
                btnupdate.setEnabled(true);
                btndelete.setEnabled(true);
                btncaripemeriksaan.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak ditemukan");
                reset();
            }
        } catch (Exception e){
            System.out.print(e);
            JOptionPane.showMessageDialog(null, "Koneksi Gagal");
        }
        
        
    }//GEN-LAST:event_btncariActionPerformed

    private void txtbayarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbayarKeyReleased
        if(txtbayar.getText().equals("") || txtbayar.getText().equals("0")){
           JOptionPane.showMessageDialog(rootPane, "Nominal Bayar Tidak Boleh Kosong!!!"); 
           txtbayar.setText(txttotal.getText());
        }else {
            int harga = Integer.parseInt(txttotal.getText());
            int bayar = Integer.parseInt(txtbayar.getText());
            int kembalian = bayar - harga;
            txtkembalian.setText(String.valueOf(kembalian));
        }
    }//GEN-LAST:event_txtbayarKeyReleased

    private void btncaripemeriksaanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncaripemeriksaanActionPerformed
        String kode = JOptionPane.showInputDialog(null, "Cari Berdasarkan Kode Pemeriksaan!!!");
        try{
            String sql = "SELECT * FROM pemeriksaan "
                    + "INNER JOIN pasien ON pasien.id_pasien=pemeriksaan.id_pasien "
                    + "INNER JOIN dokter ON dokter.id_dokter=pemeriksaan.id_dokter "
                    + "WHERE kode_periksa='"+kode+"'";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            if(res.next()){
                JOptionPane.showMessageDialog(null, "Data ditemukan");
                txtkodeperiksa.setText(res.getString(1));
                txtnamapasien.setText(res.getString(8));
                txtnamadokter.setText(res.getString(14));
                txttindakan.setText(res.getString(6));
                
                rawat = res.getString(6);
                kodeperiksa = res.getString(1);
                
                if(rawat.equals("Rawat Jalan")){
                    try{
                        String sqlr = "SELECT * FROM rawat_jalan "
                                + "WHERE kode_periksa='"+kodeperiksa+"'";
                        java.sql.Connection connr=(Connection)Koneksi.configDB();
                        java.sql.Statement stmr = connr.createStatement();
                        java.sql.ResultSet resr = stmr.executeQuery(sqlr);

                        if(resr.next()){
                            txttotal.setText(resr.getString(5));
                        } else {
                            JOptionPane.showMessageDialog(null, "Data Tidak ditemukan");
                            reset();
                        }
                    } catch (Exception e){
                        System.out.print(e);
                        JOptionPane.showMessageDialog(null, "Koneksi Gagal");
                    } 
                
                }else if(rawat.equals("Rawat Inap")){
                    try{
                        String sqlr = "SELECT * FROM rawat_inap "
                                + "WHERE kode_periksa='"+kodeperiksa+"'";
                        java.sql.Connection connr=(Connection)Koneksi.configDB();
                        java.sql.Statement stmr = connr.createStatement();
                        java.sql.ResultSet resr = stmr.executeQuery(sqlr);

                        if(resr.next()){
                            txttotal.setText(resr.getString(6));
                        } else {
                            JOptionPane.showMessageDialog(null, "Data Tidak ditemukan");
                            reset();
                        }
                    } catch (Exception e){
                        System.out.print(e);
                        JOptionPane.showMessageDialog(null, "Koneksi Gagal");
                    } 
                
                }
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak ditemukan");
                reset();
            }
        } catch (Exception e){
            System.out.print(e);
            JOptionPane.showMessageDialog(null, "Koneksi Gagal");
        }
    }//GEN-LAST:event_btncaripemeriksaanActionPerformed

