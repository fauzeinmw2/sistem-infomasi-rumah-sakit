    private void lblButtonLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblButtonLoginMouseClicked
        String userName = "fauzein";
        String password = "admin";
        
        if(txtUsername.getText().equals(userName) && txtPassword.getText().equals(password)){
            JOptionPane.showMessageDialog(null, "Login Berhasil");
            MainForm main = new MainForm();
            main.setVisible(true);
            
            DashboardForm dashboard = new DashboardForm();
            main.DesktopPane.add(dashboard);
            Dimension gs = main.DesktopPane.getSize();
            Dimension cs = dashboard.getSize();

            dashboard.setLocation((gs.width - cs.width) / 2, (gs.height - cs.height) / 2);

            dashboard.setVisible(true);
            this.dispose();
            
        }else{
            JOptionPane.showMessageDialog(null, "Username / Password Salah !!!");
        }
    }//GEN-LAST:event_lblButtonLoginMouseClicked

