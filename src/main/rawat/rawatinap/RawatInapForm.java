    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        if(txtkoderawat.getText().equals("") || txtkodeperiksa.getText().equals("") || txtnamapasien.getText().equals("") || cbtipekamar.getSelectedIndex() ==  0 || cbkamar.getSelectedIndex() ==  0 || dttanggalmasuk.getDate().equals("")){
            JOptionPane.showMessageDialog(null, "Form Selain Tanggal Keluar dan Total Biaya Tidak Boleh Ada Yang Kosong!!!");
        }else{
            try {
                
                String kode_kamar = (String) kodeKamar.get((int)cbkamar.getSelectedIndex());
                
                String sql = "INSERT INTO rawat_inap(id_rawat_inap, kode_periksa, kode_kamar, tgl_masuk, harga) "
                    + "VALUES('"+txtkoderawat.getText()+"', '"+txtkodeperiksa.getText()+"', '"+kode_kamar+"', '"+tanggalMasuk+"', '"+txtharga.getText()+"')";
                java.sql.Connection conn = (Connection)Koneksi.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                JOptionPane.showMessageDialog(null, "Prosess Simpan Data Rawat Inap Berhasil..");
                reset();
                kodeRawat();

            }catch (HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

