    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        String kode = JOptionPane.showInputDialog(null, "Cari Berdasarkan Kode Rawat Inap!!!");
        try{
            String sql = "SELECT * FROM rawat_inap "
                    + "INNER JOIN pemeriksaan ON pemeriksaan.kode_periksa=rawat_inap.kode_periksa "
                    + "INNER JOIN pasien ON pasien.id_pasien=pemeriksaan.id_pasien "
                    + "INNER JOIN kamar ON kamar.kode_kamar=rawat_inap.kode_kamar "
                    + "WHERE id_rawat_inap='"+kode+"'";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            if(res.next()){
                
//                int kode_kamar = kodeKamar.indexOf(res.getString(18));
                
                JOptionPane.showMessageDialog(null, "Data ditemukan");
                txtkoderawat.setText(res.getString(1));
                txtkodeperiksa.setText(res.getString(2));
                txtnamapasien.setText(res.getString(14));
                cbtipekamar.setSelectedItem(res.getString(21));
                cbkamar.setSelectedItem(res.getString(20));
                
                java.util.Date tglmasuk = new SimpleDateFormat("yyy-MM-dd").parse((String)res.getString(4));
                dttanggalmasuk.setDate(tglmasuk);
                
                if(res.getString(5) != null){
                    java.util.Date tglkeluar = new SimpleDateFormat("yyy-MM-dd").parse((String)res.getString(5));
                    dttanggalkeluar.setDate(tglkeluar);
                }else{
                    dttanggalkeluar.setDate(null);
                }
                
                txtharga.setText(res.getString(6));
                
                btnsimpan.setEnabled(false);
                btnupdate.setEnabled(true);
                btndelete.setEnabled(true);
                btncaripemeriksaan.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak ditemukan");
                reset();
            }
        } catch (Exception e){
            System.out.print(e);
            JOptionPane.showMessageDialog(null, "Koneksi Gagal");
        }
    }//GEN-LAST:event_btncariActionPerformed

