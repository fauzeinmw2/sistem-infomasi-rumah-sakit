    private void tampilKamar(){
        try{
            String sql = "SELECT * FROM kamar WHERE jenis_kamar='"+cbtipekamar.getSelectedItem()+"'";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                cbkamar.addItem(res.getString(2));
                kodeKamar.add(res.getString(1));
            }
            
        }catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }
    }
    
    private void kodeRawat(){
        try{
            String sql = "SELECT MAX(right(id_rawat_inap, 1)) AS id_rawat_inap FROM rawat_inap";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            if(res.next()){
                int kode = res.getInt(1);
                String AN = "" + (kode + 1);
                String Nol = "";
                
                if(AN.length() == 1){
                    Nol = "00";
                }else if(AN.length() == 2){
                    Nol = "0";
                }else if(AN.length() == 3){
                    Nol = "";
                }
                
                txtkoderawat.setText("RWI" + Nol + AN);
                
            } else {
                txtkoderawat.setText("RWI001");
            }
        } catch (Exception e){
            System.out.print(e);
            JOptionPane.showMessageDialog(null, "Koneksi Gagal");
        }
    }
    
